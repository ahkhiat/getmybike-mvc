
<div class="container">
    <h1>Foire Aux Questions (FAQ)</h1>

    <div class="faq-section">
        <h2>1. Qu'est-ce que Get my Bike ?</h2>
        <p>Get my Bike est une plateforme en ligne qui permet aux particuliers de louer leurs motos à d'autres passionnés. Que vous cherchiez à louer une moto pour une journée ou pour une plus longue période, notre site vous met en relation avec des propriétaires de motos près de chez vous.</p>
    </div>

    <div class="faq-section">
        <h2>2. Comment fonctionne la location de motos ?</h2>
        <ul>
            <li><strong>Inscription :</strong> Inscrivez-vous gratuitement sur Get my Bike.</li>
            <li><strong>Réservation :</strong> Faites une demande de réservation auprès du propriétaire de la moto.</li>
            <li><strong>Location :</strong> Après acceptation de la réservation, organisez les détails de la prise en main et de la restitution de la moto avec le propriétaire.</li>
            <li><strong>Profitez :</strong> Roulez en toute liberté et profitez de votre expérience !</li>
        </ul>
    </div>

    <div class="faq-section">
        <h2>3. Quelles sont les conditions pour louer une moto ?</h2>
        <ul>
            <li><strong>Âge minimum :</strong> Avoir au moins 21 ans (ou selon la législation locale).</li>
            <li><strong>Permis :</strong> Posséder un permis de conduire valide pour le type de moto loué.</li>
            <li><strong>Expérience :</strong> Certains propriétaires peuvent exiger une expérience minimale de conduite.</li>
        </ul>
    </div>

    <div class="faq-section">
        <h2>4. Comment puis-je mettre ma moto en location ?</h2>
        <ul>
            <li><strong>Inscription :</strong> Inscrivez-vous et créez un compte.</li>
            <li><strong>Annonce :</strong> Publiez une annonce détaillée de votre moto avec des photos, une description, et les conditions de location.</li>
            <li><strong>Gestion :</strong> Gérez les demandes de réservation et communiquez avec les locataires potentiels via la plateforme.</li>
        </ul>
    </div>

   


    <div class="faq-section">
        <h2>9. Puis-je annuler une réservation ?</h2>
        <ul>
            <li><strong>Annulation par le locataire :</strong> Les politiques d'annulation varient. Vérifiez les conditions spécifiques de chaque annonce avant de réserver.</li>
            <li><strong>Annulation par le propriétaire :</strong> Le propriétaire peut annuler sous certaines conditions, et dans ce cas, nous vous aiderons à trouver une alternative.</li>
        </ul>
    </div>

